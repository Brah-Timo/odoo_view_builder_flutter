// Initialisation order:
//   1. WidgetsFlutterBinding
//   2. CrashReporter  — must be set up BEFORE runApp so uncaught errors
//      from widget building are captured.
//   3. AnalyticsService
//   4. FlutterError.onError override
//   5. runZonedGuarded → runApp(ProviderScope(…))
//
// Firebase note
// ─────────────
// Firebase is OPTIONAL. To enable it:
//   1. Run `flutterfire configure`.
//   2. Uncomment the firebase_core / analytics / crashlytics lines in pubspec.yaml.
//   3. Uncomment the `Firebase.initializeApp` call below.
//   4. Pass `useFirebase: true` to `CrashReporter.init()` and `AnalyticsService.init()`.

